    public record DeploymentArtifacts(
        String dockerCompose,
        String kubernetesYaml,
        String helmValues,
        String strimziKafkaConnector,
        String terraform,
        Map<String, String> additionalFiles
    ) {}

    public DeploymentArtifacts generate(PipelineSpec spec) {
        Map<String, String> additional = new HashMap<>();
        
        String dockerCompose = generateDockerCompose(spec);
        String kubernetesYaml = generateKubernetes(spec);
        String helmValues = generateHelmValues(spec);
        String strimziConnector = generateStrimziConnector(spec);
        String terraform = generateTerraform(spec);

        additional.put("README.md", generateReadme(spec));
        additional.put("deploy.sh", generateDeployScript(spec));
        additional.put(".env.example", generateEnvExample(spec));

        return new DeploymentArtifacts(
            dockerCompose,
            kubernetesYaml,
            helmValues,
            strimziConnector,
            terraform,
            additional
        );
    }

