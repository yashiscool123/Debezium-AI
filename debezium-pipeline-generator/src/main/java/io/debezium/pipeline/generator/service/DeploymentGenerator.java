    private String generateDeployScript(PipelineSpec spec) {
        StringBuilder sb = new StringBuilder();
        sb.append("#!/bin/bash\n");
        sb.append("# Deploy script for ").append(spec.name()).append("\n");
        sb.append("# Generated by Debezium Pipeline Generator\n\n");
        
        sb.append("set -e\n\n");
        
        sb.append("DEPLOYMENT_TYPE=\"${1:-docker-compose}\"\n\n");
        
        sb.append("case $DEPLOYMENT_TYPE in\n");
        sb.append("  docker-compose)\n");
        sb.append("    docker-compose up -d\n");
        sb.append("    ;;\n");
        sb.append("  kubernetes)\n");
        sb.append("    kubectl apply -f kubernetes/\n");
        sb.append("    ;;\n");
        sb.append("  helm)\n");
        sb.append("    helm install debezium ./helm -f values.yaml\n");
        sb.append("    ;;\n");
        sb.append("  terraform)\n");
        sb.append("    terraform init && terraform apply\n");
        sb.append("    ;;\n");
        sb.append("  *)\n");
        sb.append("    echo \"Unknown deployment type: $DEPLOYMENT_TYPE\"\n");
        sb.append("    echo \"Usage: $0 [docker-compose|kubernetes|helm|terraform]\"\n");
        sb.append("    exit 1\n");
        sb.append("    ;;\n");
        sb.append("esac\n\n");
        
        sb.append("echo \"Deployment complete!\"\n");
        sb.append("echo \"Kafka Connect: http://localhost:8083\"\n");
        sb.append("echo \"Schema Registry: http://localhost:8081\"\n");
        
        return sb.toString();
    }

    private String generateEnvExample(PipelineSpec spec) {
        StringBuilder sb = new StringBuilder();
        sb.append("# Environment variables for ").append(spec.name()).append("\n");
        sb.append("# Copy to .env and fill in values\n\n");
        
        if (spec.sourceConnector().config() != null) {
            for (Map.Entry<String, String> entry : spec.sourceConnector().config().entrySet()) {
                if (entry.getKey().contains("password") || entry.getKey().contains("secret") || entry.getKey().contains("key")) {
                    String envKey = entry.getKey().toUpperCase().replace(".", "_");
                    sb.append(envKey).append("=your-secret-here\n");
                }
            }
        }
        
        sb.append("\nKAFKA_BOOTSTRAP_SERVERS=localhost:9092\n");
        sb.append("SCHEMA_REGISTRY_URL=http://localhost:8081\n");
        
        return sb.toString();
    }

    private String toJson(Map<String, String> map) {
        if (map == null || map.isEmpty()) return "{}";
        StringBuilder sb = new StringBuilder();
        sb.append("{\n");
        int i = 0;
        for (Map.Entry<String, String> entry : map.entrySet()) {
            sb.append("  \"").append(entry.getKey()).append("\": \"").append(entry.getValue()).append("\"");
            if (i < map.size() - 1) sb.append(",");
            sb.append("\n");
            i++;
        }
        sb.append("}");
        return sb.toString();
    }
