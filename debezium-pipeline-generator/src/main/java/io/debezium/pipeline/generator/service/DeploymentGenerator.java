    private String generateStrimziConnector(PipelineSpec spec) {
        StringBuilder sb = new StringBuilder();
        sb.append("# Strimzi KafkaConnector Custom Resource\n");
        sb.append("# Generated by Debezium Pipeline Generator\n");
        sb.append("apiVersion: kafka.strimzi.io/v1beta2\n");
        sb.append("kind: KafkaConnector\n");
        sb.append("metadata:\n");
        sb.append("  name: ").append(spec.sourceConnector().name()).append("\n");
        sb.append("  labels:\n");
        sb.append("    strimzi.io/cluster: ").append(spec.deployment().connectClusterName()).append("\n");
        sb.append("spec:\n");
        sb.append("  class: ").append(spec.sourceConnector().connectorClass()).append("\n");
        sb.append("  tasksMax: 1\n");
        sb.append("  config:\n");
        
        if (spec.sourceConnector().config() != null) {
            for (Map.Entry<String, String> entry : spec.sourceConnector().config().entrySet()) {
                sb.append("    ").append(entry.getKey()).append(": \"").append(entry.getValue()).append("\"\n");
            }
        }
        
        if (spec.sourceConnector().transforms() != null) {
            int i = 0;
            for (TransformConfig transform : spec.sourceConnector().transforms()) {
                sb.append("    transforms: \"").append(transform.name()).append("\"\n");
                sb.append("    transforms.").append(transform.name()).append(".type: ").append(transform.type()).append("\n");
                if (transform.config() != null) {
                    for (Map.Entry<String, String> entry : transform.config().entrySet()) {
                        sb.append("    transforms.").append(transform.name()).append(".").append(entry.getKey()).append(": \"").append(entry.getValue()).append("\"\n");
                    }
                }
                if (transform.predicate() != null) {
                    sb.append("    transforms.").append(transform.name()).append(".predicate: \"").append(transform.predicate()).append("\"\n");
                }
                i++;
            }
        }
        
        if (spec.sourceConnector().predicates() != null) {
            for (Map.Entry<String, String> entry : spec.sourceConnector().predicates().entrySet()) {
                sb.append("    predicates.").append(entry.getKey()).append(": \"").append(entry.getValue()).append("\"\n");
            }
        }
        
        return sb.toString();
    }

